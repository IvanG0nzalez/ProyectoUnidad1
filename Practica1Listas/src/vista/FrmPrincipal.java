    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
            // TODO add your handling code here:
        if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtFechaNacimiento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);                        
        } else {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate fechaNacimiento = LocalDate.parse(txtFechaNacimiento.getText(), formatter);
            if (fechaNacimiento.isAfter(LocalDate.now()) || fechaNacimiento.isBefore(LocalDate.of(1875, 2, 21))){
                JOptionPane.showMessageDialog(this, "La fecha ingresada no es correcta", "Error", JOptionPane.ERROR_MESSAGE);
            } else guardar();
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

