    private void limpiar() {
        cargarCombo();
        cargarTabla();
        txtNombre.setText("");
        txtApellido.setText("");
        txtFechaNacimiento.setText("");
    }

    private void guardar() {

        Persona persona = new Persona();
        
        if(txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtFechaNacimiento.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);            
        } else {
            persona.setNombre(txtNombre.getText());
            persona.setApellido(txtApellido.getText());
            persona.setFechaNacimiento(txtFechaNacimiento.getText());
            persona.setGenero(Utilidades.obtenerGenero(cbxGenero));
            try {
                pc.calcularEdad(persona);
                pc.clasificarPorEdad(persona);
                pc.getPersonas().insertar(persona);
                limpiar();
            } catch (Exception e) {
            }
        }
    }

