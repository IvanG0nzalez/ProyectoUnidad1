    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if(!pc.getPersona().getNombre().equalsIgnoreCase(PersonaController.VALORDEFECTO)){
            Integer i = JOptionPane.showConfirmDialog(this,"¿Está seguro de que desea cambiar la información?");
            if(i == JOptionPane.OK_OPTION){
                 DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                 LocalDate fechaNacimiento = LocalDate.parse(txtFechaNacimiento.getText(), formatter);
                if (fechaNacimiento.isAfter(LocalDate.now()) || fechaNacimiento.isBefore(LocalDate.of(1875, 2, 21))){
                    JOptionPane.showMessageDialog(this, "La fecha ingresada no es correcta", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    actualizarInfoPersona();
                    limpiar();
                    jPanel2.setVisible(false);
                    jPanel5.setVisible(false);
                } 
            }
        } else {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate fechaNacimiento = LocalDate.parse(txtFechaNacimiento.getText(), formatter);
            if (fechaNacimiento.isAfter(LocalDate.now()) || fechaNacimiento.isBefore(LocalDate.of(1875, 2, 21))){
                JOptionPane.showMessageDialog(this, "La fecha ingresada no es correcta", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
            actualizarInfoPersona();
            limpiar();
            jPanel2.setVisible(false);
            jPanel5.setVisible(false);
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

