    private void actualizarInfoPersona(){
        pc.getPersona().setNombre(txtNombre.getText());
        pc.getPersona().setApellido(txtApellido.getText());
        pc.getPersona().setGenero(Utilidades.getGeneroCombo(cbxGenero).toString());
        try {
               pc.getPersona().setFechaNacimiento(txtFechaNacimiento.getText());
               pc.getPersona().setEdad(pc.calcularEdad(pc.getPersona()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        pc.getPersona().setClasificacion(pc.clasificarPorEdad(pc.getPersona()));
        
    }
    
    private void crearArregloPersona(){
        pc = new PersonaController(Integer.parseInt(cbxNroPersonas.getSelectedItem().toString()));
        btnCrear.setEnabled(false);
        cargarTabla();
    }
    
