    private void tblPersonasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPersonasMouseClicked
        // TODO add your handling code here:
        Utilidades.seleccionarPersona(tblPersonas, txtPracticas);
        fila = tblPersonas.getSelectedRow();
        if(fila >= 0){
            pc.setPersona(modeloTabla.getPersonas()[fila]);
            txtNombre.setText(pc.getPersona().getNombre());
            txtApellido.setText(pc.getPersona().getApellido());
            txtFechaNacimiento.setText(pc.getPersona().getFechaNacimiento());
            cbxGenero.setSelectedItem(pc.getPersona().getGenero());
            txtPracticas.setText(pc.getPersona().getPracticas());
            cargarEditarPersona();
            cargarPracticas();
        } else JOptionPane.showMessageDialog(this, "Seleccione un elemento de la tabla", "Error", JOptionPane.ERROR_MESSAGE);
       
    }//GEN-LAST:event_tblPersonasMouseClicked

